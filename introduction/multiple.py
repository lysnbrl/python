"""We can create multiple variables in a single line of code, we simply need
to separate the variable names and the values to store with commas. Values 
will be stored according to their position"""
title, name, last_name = "Mx.", "Jade", "Navarro"

"""We also can use the same method when we use the print() function, this 
will print all the variables in the same line"""
print(title, name, last_name)

"""Note: It is better if we don't create more than four variables in a
single line of code, to avoid making the code difficult to read"""

